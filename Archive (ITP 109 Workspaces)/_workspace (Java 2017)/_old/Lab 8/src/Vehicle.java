public class Superhero {
	private String name;
	private int healthLeft;
	private int attackStength;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHealthLeft() {
		return healthLeft;
	}
	public void setHealthLeft(int healthLeft) {
		this.healthLeft = healthLeft;
	}
	public int getAttackStength() {
		return attackStength;
	}
	public void setAttackStength(int attackStength) {
		this.attackStength = attackStength;
	}
	
	
}
